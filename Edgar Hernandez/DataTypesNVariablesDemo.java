package com.amdocs;

public class DataTypesNVariablesDemo {

	public static void main(String[] args) {
		int wholeNumber;
		wholeNumber = 10;

		float decimalNumber1 = 12.36f;
		double decimalNumber2 = 199.7353;
		char singleAlphabet = 'a';
		boolean value = true;
		long longValue = 263722736;
		short shortValue = 1234;

		System.out.println("The value in Integer is: " + wholeNumber);
		System.out.println("The value in float is: " + decimalNumber1);
		System.out.println("The value in double is: " + decimalNumber2);
		System.out.println("The value in Char is: " + singleAlphabet + "\nThe value in Boolean is: " + value);
		System.out.println("The value in long is: " + longValue + "\nThe value in short is: " + shortValue);
		
		
	}

}
